def strip_refs(t):
    # يشيل علامات المصادر الداخلية زي [679، 680] أو [12، 75]
    return re.sub(r"\s*\[[\d\s،,]+\]", "", t)

def b(t):
    # **bold** -> <strong>bold</strong>  (بعد escape)
    t = strip_refs(t)
    parts = t.split("**")
    out = ""
    for i, p in enumerate(parts):
        p = html.escape(p)
        out += f"<strong>{p}</strong>" if i % 2 == 1 else p
    return out

def esc(t):
    return html.escape(strip_refs(t))

